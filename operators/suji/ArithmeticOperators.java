package com.operators.suji;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int a=50;
		int b=28;
		System.out.println("additon operation:"+(a+b));
		System.out.println("subtraction operation:"+(a-b));
		System.out.println("multiplication operation:"+(a*b));
		System.out.println("division operation:"+(a/b));
		System.out.println("Modules operation:"+(a%b));
		System.out.println("increment operation:"+(a++));
		System.out.println("decrement operation:"+(a--));
	}

}
