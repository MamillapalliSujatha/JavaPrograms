package com.arrays.programs;

public class WaysToPrintArray {

	public static void main(String[] args) {
		int arr[]= {2,4,6,3,8};
		//Print Array using for loop
        for(int i = 0;i < arr.length;i++) {
			System.out.print(arr[i]+ " ");
        }
        System.out.println();
        System.out.println(arr[0]);
	}

}
