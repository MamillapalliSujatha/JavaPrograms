package com.strings.programs;

public class CheckStringIsNull {

	public static void main(String[] args) {
		String s1=null;
		String s2="";
		if(s1==null)
			System.out.println("String is null");
		else
			System.out.println("String is not null");
		
		if(s2==null)
			System.out.println("String is null");
		else
			System.out.println("String is not null");
		
	}

}
